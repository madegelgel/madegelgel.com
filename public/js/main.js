document.addEventListener('DOMContentLoaded', function() {
  const navToggle = document.getElementById('nav-toggle');
  const mobileMenu = document.getElementById('mobile-menu');
  const closeMenu = document.getElementById('close-menu');
  const menuOverlay = document.getElementById('menu-overlay');
  const desktopNavContent = document.getElementById('desktop-nav-content');

  // Toggle mobile menu open
  if (navToggle && mobileMenu && menuOverlay) {
    navToggle.addEventListener('click', function() {
      mobileMenu.classList.remove('translate-x-full');
      mobileMenu.classList.add('translate-x-0');
      menuOverlay.classList.remove('hidden');
      menuOverlay.classList.add('opacity-50');
    });
  }

  // Toggle mobile menu close
  if (closeMenu && mobileMenu && menuOverlay) {
    closeMenu.addEventListener('click', function() {
      mobileMenu.classList.remove('translate-x-0');
      mobileMenu.classList.add('translate-x-full');
      menuOverlay.classList.remove('opacity-50');
      menuOverlay.classList.add('hidden');
    });
  }

  // Close menu when clicking outside (on overlay)
  if (menuOverlay && mobileMenu) {
    menuOverlay.addEventListener('click', function() {
      mobileMenu.classList.remove('translate-x-0');
      mobileMenu.classList.add('translate-x-full');
      menuOverlay.classList.remove('opacity-50');
      menuOverlay.classList.add('hidden');
    });
  }

  // Handle desktop nav visibility (re-enable if needed, based on previous state)
  // This ensures desktop nav is visible when resizing from mobile to desktop
  if (desktopNavContent) {
    window.addEventListener('resize', function() {
      if (window.innerWidth >= 1024) { // Tailwind's 'lg' breakpoint
        desktopNavContent.classList.remove('hidden');
      } else {
        // Keep hidden on mobile if it was hidden by default
        // desktopNavContent.classList.add('hidden'); // Only if you want to hide it on mobile by default
      }
    });
  }
});